# How does Treiner work?

Similar to online comparison and booking platforms and existing as the only soccer dedicated coaching booking platform in Australia, Treiner serves as a marketplace for male and female players, parents & teams of all ages and abilities to book private sessions with experienced coaches. 

# How do I receive bookings?

By making an account on our platform, players are able to find you in their area and book a session depending on your specialisations and their needs. Our marketing team will also interview and promote you for free via our organic and paid digital marketing campaigns.


# Who is Treiner designed for?

The predominant client audience are parents with adolescent children in primary school or high school who play competitive soccer or are seeking quality coaching. Players & teams from various age groups use the platform however, so you may have clubs, academies and schools contacting you, as well as adult soccer players who wish to be trained on the platform.

# How do payments work?

Once players have chosen the coach and booked the session, payments can be made online via our Stripe integration or with cash. The platform also collects feedback and data relating to training sessions, to better improve services. 

# How are session details communicated?

Once the player has booked the session, details are sent through to you straight away. Reminders are also sent via email and text message 24 hours before the session.

